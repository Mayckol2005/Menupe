def suma(num1,num2):
    suma = (num1 + num2);
    print(f"Suma: {suma}")

def resta(num1,num2):
    resta = (num1 - num2);
    print(f"Resta: {resta}");

def multi(num1,num2):
    multi = (num1 * num2);
    print(f"Multiplicacion: {multi}");

def division(num1,num2):
    division = (num1 / num2);
    print(f"Division: {division}");